/**
 * Get cities for a specific level (1-10)
 * Each level has exactly 50 cities
 *
 * @param {number} level - Level number (1-10)
 * @returns {Array<City>} Array of cities for the specified level
 * @throws {Error} If level is not between 1 and 10
 */
function getCitiesForLevel(level) {
  if (level < 1 || level > 10) {
    throw new Error('Level must be between 1 and 10');
  }

  const startIndex = (level - 1) * 50;
  const endIndex = level * 50;

  return CITIES.slice(startIndex, endIndex);
}

/**
 * Select random cities for a level (used in single-player mode)
 *
 * @param {number} level - Level number (1-10)
 * @param {number} [count=5] - Number of cities to select
 * @returns {Array<City>} Array of randomly selected cities
 */
function selectCitiesForLevel(level, count = 5) {
  const levelCities = getCitiesForLevel(level);
  const shuffled = [...levelCities].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

/**
 * Select random cities based on difficulty (used in multiplayer mode)
 *
 * @param {'easy'|'medium'|'hard'} difficulty - Game difficulty level
 * @param {number} [count=5] - Number of cities to select
 * @returns {Array<City>} Array of randomly selected cities
 */
function selectRandomCities(difficulty, count = 5) {
  // Map difficulty to level ranges
  let levelRange;
  if (difficulty === 'easy') {
    // Easy: levels 1-3 (famous, well-known cities)
    levelRange = [1, 2, 3];
  } else if (difficulty === 'hard') {
    // Hard: levels 8-10 (obscure, challenging cities)
    levelRange = [8, 9, 10];
  } else {
    // Medium: levels 4-7 (moderately known cities)
    levelRange = [4, 5, 6, 7];
  }

  // Collect all cities from the level range
  const availableCities = [];
  for (const level of levelRange) {
    availableCities.push(...getCitiesForLevel(level));
  }

  // Shuffle and select
  const shuffled = [...availableCities].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

